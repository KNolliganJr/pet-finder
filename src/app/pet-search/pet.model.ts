export class Pet {
    public name: string;
    public animal: string;
    public age: string;
    public sex: string;
    public size: string;
  
    constructor(name: string, animal: string, age: string, sex: string, size: string) {
      this.name = name;
      this.animal = animal;
      this.age = age;
      this.sex = sex;
      this.size = size;
    }
  }